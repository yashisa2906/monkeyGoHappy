var backG;
var backgImg;
var monkey;
var monkeyImg;
var ground;

var invisibleGround;
var bananaImg
var bananagroup;
var PLAY = 1;
var END = 0;
var gameState = PLAY;
var rockImg;
var score = 0;
var rockgroup;
var sizeincreased = false;
var sizedecreased = false;
var banana;

var bananaCollected = 0;


function preload() {
monkeyImg=loadAnimation("sprite_1.png","sprite_2.png","sprite_3.png","sprite_4.png","sprite_5.png","sprite_6.png","sprite_7.png","sprite_8.png")
  bananaImg = loadImage("banana.png");
  rockImg = loadImage("obstacle.png");
  backgImg = loadImage("jungle.jpg");
  monkeyCollided = loadImage("sprite_0.png");
}


function setup() {
  createCanvas(600, 300);
  backG = createSprite(300, 100);
  backG.addImage("back", backgImg);
  backG.scale = 1;
  bananagroup = new Group();
  ground = createSprite(300, 600, 3000, 200);
  ground.shapeColor = 'olivedrab';
  invisibleGround = createSprite(200, 295, 600, 5);
  invisibleGround.visible = false;
  monkey = createSprite(60, 280);
  monkey.addAnimation("monkey", monkeyImg);
  monkey.scale = 0.17;
  rockgroup = new Group();

  
  
}

function draw() {
  background(255);
  
  
  if (gameState === PLAY) {
    score = Math.round(score + 0.5);
    ground.velocityX = -6;
    if (ground.x < 0) {
      ground.x = ground.width / 2;
    }
    backG.velocityX = -(6 + 1.5 * score / 100);
    if (backG.x < 90) {
      backG.x = backG.width / 2;
    }
    if (keyDown("space") && monkey.y >= 230) {
      monkey.velocityY = -12;
     
    }
    monkey.velocityY = monkey.velocityY + 0.7;
    spawnbanana();
    spawnrocks();
    for (var i = 0; i < bananagroup.length; i++) {
      banana = bananagroup.get(i);
      if (banana.isTouching(monkey)) {
        banana.destroy();
       
        bananaCollected = bananaCollected + 1;
      }
    }
    if (rockgroup.isTouching(monkey)) {

      if (sizedecreased === false) {
        monkey.scale = monkey.scale - 0.01;
        sizedecreased = true;
        gameState=END;
        
      }
    }
    if (rockgroup.isTouching(monkey) == false) {
      sizedecreased = false;
    }
    if (bananaCollected % 5 === 0 && bananaCollected > 0) {
      if (sizeincreased === false) {
        monkey.scale = monkey.scale + 0.05;
        sizeincreased = true;
      }
    } else {
      sizeincreased = false;
    }
  }
  monkey.collide(invisibleGround);
  drawSprites();
  fill("white");
  textSize(25);
  text("score: "+bananaCollected,170,25);
  stroke("black");
  
  if(gameState===END){
  if(rockgroup.isTouching(monkey) ){
    monkey.velocityY=0;
    ground.velocityX=0;
    backG.velocityX=0;
    rockgroup.velocityX=0;
    bananagroup.velocityX=0;
    fill("yellow")
    textSize(30);
    text("GAME OVER" ,300,150);
    monkey.addImage("monkeyCollided")
   monkey.scale=0.09;
    
    
    
    
  }
  }
  
}

function spawnbanana() {
  if (frameCount % 60 === 0) {
    var banana = createSprite(650, random(200, 90));
    banana.addImage("banana", bananaImg);
    banana.scale = 0.09;
    banana.velocityX = -(random(3, 15) + 1.5 * score / 100);
    banana.lifetime = 230;
    banana.depth = monkey.depth;
    banana.depth = monkey.depth + 1;
    bananagroup.add(banana);
  }
}

function spawnrocks() {
  if (frameCount % 80 === 0) {
    var rock = createSprite(random(650, 800), 280, 10, 40);
    rock.addImage("rock", rockImg);
    rock.scale = 0.15;
    rock.velocityX = -(6 + 1.5 * score / 100);
    rock.lifetime = 130;
    rockgroup.add(rock);
  }
}




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































